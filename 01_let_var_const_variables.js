// var as variable - old method
var myAge = 20;
console.log(myAge);


// let as variable - new method
let myAge = 20;
console.log(myAge);


// const as variable - new method
const myAge = 20;
console.log(myAge);


// var can be updated
var myAge = 20;
console.log(myAge);
myAge = 30;
console.log(myAge); // No error


// let can be updated
let myAge = 20;
console.log(myAge);
myAge = 30;
console.log(myAge); // No error


// let can be updated
let myAge = 20;
console.log(myAge);
myAge = 30;
console.log(myAge); // No error


// const cannot be updated
const myAge = 20;
console.log(myAge);
myAge = 30;
console.log(myAge); // Error
